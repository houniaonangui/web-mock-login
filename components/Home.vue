export default {
  components:{
    
  },
  data() {
    return {
      images: [
        { src: '../assets/01.png', alt: 'æ”»ç•¥å¤§å…¨' },
        { src: '../assets/02.png', alt: 'è¿åŠ¨å¬é›†' },
        { src: '../assets/03.png', alt: 'åæ§½å¤§ä¼š' },
        { src: '../assets/04.png', alt: 'å¤±ç‰©æ‹›é¢†' },
        { src: '../assets/05.png', alt: 'å¥½ç‰©æ¨è' },
        { src: '../assets/pet.png', alt: 'å·å¸ˆæ ¡å® ' }
      ],
      m: [],
      index: 0,
      time: null
    };
  },
  mounted() {
    this.initializeM();
    this.timer();
  },
  methods: {
    initializeM() {
      this.m = Array.from({ length: this.images.length });
    },
    position() {
      const images = this.$el.querySelector('.images');
      images.style.left = `${this.index * -100}%`;
    },
    add() {
      if (this.index >= this.m.length - 1) {
        this.index = 0;
      } else {
        this.index++;
      }
    },
    desc() {
      if (this.index < 1) {
        this.index = this.m.length - 1;
      } else {
        this.index--;
      }
    },
    prevSlide() {
      this.desc();
      this.position();
      this.clearTimer();
      this.timer();
    },
    nextSlide() {
      this.add();
      this.position();
      this.clearTimer();
      this.timer();
    },
    goToSlide(index) {
      this.index = index;
      this.position();
      this.clearTimer();
      this.timer();
    },
    timer() {
      this.time = setInterval(() => {
        this.index++;
        this.desc();
        this.add();
        this.position();
      }, 3000);
    },
    clearTimer() {
      clearInterval(this.time);
    }
  },
  beforeDestroy() {
    this.clearTimer();
  }
};
